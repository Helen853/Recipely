import UIKit

/// Расширение для цветов во всем приложении
extension CGColor {
    /// Серый цвет для границы
    static let grayForBorder = UIColor.rgb(208, 212, 218).cgColor
    /// Белый цвет для градиента
    static let whiteForGradient = UIColor.rgb(255, 255, 255).cgColor
    /// Бирюзовый для градиента
    static let turquoiseForGradient = UIColor.rgb(222, 238, 229).cgColor
    /// Бирюзовый цвет
    static let turquoise = UIColor.rgb(112, 185, 190).cgColor
}
